package com.example.demo.Entity;

public class Another {
private long id;
private String name;
private String age;

public Another() {
	super();
}
public Another(long id, String name, String age) {
	super();
	this.id = id;
	this.name = name;
	this.age = age;
}
public long getId() {
	return id;
}
public void setId(long id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}


}
